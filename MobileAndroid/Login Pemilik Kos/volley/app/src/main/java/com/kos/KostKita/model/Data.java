package com.kos.KostKita.model;

public class Data {

	private String nama_kos, alamat_kos, jenis_kos, stok, imgURL;

	public String getNama_kos() {
		return nama_kos;
	}
	public void setNama_kos(String nama_kos) {
		this.nama_kos = nama_kos;
	}
	public String getAlamat_kos() {
		return alamat_kos;
	}
	public void setAlamat_kos(String alamat_kos) {
		this.alamat_kos = alamat_kos;
	}
	public String getJenis_kos() {
		return jenis_kos;
	}
	public void setJenis_kos(String jenis_kos) {
		this.jenis_kos = jenis_kos;
	}
	public String getStok() {
		return stok;
	}
	public void setStok(String stok) {
		this.stok = stok;
	}
	public String getImgURL(){ return imgURL; }
	public void setImgURL(String imgURL){ this.imgURL = imgURL; }

}
